function expandNav() {
  sn = document.getElementById("_sidenav");
  snc = document.getElementById("_sidenav-container");
  sn.style.width = "200px";
  snc.style.margin = "0 10%";
}

function shrinkNav() {
  sn = document.getElementById("_sidenav");
  snc = document.getElementById("_sidenav-container");
  sn.style.width = "80px";
  snc.style.margin = "0 30%";
}
